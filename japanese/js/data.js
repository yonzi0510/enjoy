  function flat(cmds) {
    const pts = [cmds[0].slice()];
    let cur = cmds[0];
    for (let i = 1; i < cmds.length; i++) {
      const c = cmds[i];
      if (Array.isArray(c) && typeof c[0] === 'number') {
        pts.push(c.slice());
        cur = c;
      } else if (c[0] === 'q') {
        const [, cx, cy, x, y] = c;
        for (let t = 1; t <= 12; t++) {
          const s = t / 12, r = 1 - s;
          pts.push([r * r * cur[0] + 2 * r * s * cx + s * s * x,
                    r * r * cur[1] + 2 * r * s * cy + s * s * y]);
        }
        cur = [x, y];
      } else if (c[0] === 'c') {
        const [, c1x, c1y, c2x, c2y, x, y] = c;
        for (let t = 1; t <= 12; t++) {
          const s = t / 12, r = 1 - s;
          pts.push([r * r * r * cur[0] + 3 * r * r * s * c1x + 3 * r * s * s * c2x + s * s * s * x,
                    r * r * r * cur[1] + 3 * r * r * s * c1y + 3 * r * s * s * c2y + s * s * s * y]);
        }
        cur = [x, y];
      }
    }
    return pts.map(p => [Math.round(p[0] * 10) / 10, Math.round(p[1] * 10) / 10]);
  }

